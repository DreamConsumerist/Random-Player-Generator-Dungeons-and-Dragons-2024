export default function generateName(
	species: Species,
	charClass: string,
	level: number
): string {
	return orcNameGen();
	switch (species.name) {
		case "Dragonborn":
			return dragonbornNameGen(species);
		case "Dwarf":
			return dwarfNameGen();
		case "Elf":
			return elfNameGen(species);
		case "Gnome":
			return gnomeNameGen(species);
		case "Goliath":
			return goliathNameGen(species);
		case "Halfling":
			return halflingNameGen();
		case "Human":
			return humanNameGen();
		case "Orc":
			return orcNameGen();
	}
	return "No race resolution";
}

